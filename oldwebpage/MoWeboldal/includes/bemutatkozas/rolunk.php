	<h2 class="CIM2">A Szövetségről</h2>

	

	<div class="row">

		<div class="col-12 col-s-12">

			<p><b>Szövetségünk 1989. szeptemberében alakult. Gyermekek, fiatalok, felnőttek</b> szervezete vagyunk. Szövetséget kötöttünk a <b>szülőföld</b>, a <b>haza</b> felfedezésére, <b>nemzeti értékeink, hagyományaink megőrzésére,</b> az <b>igaz emberi értékek követésére</b>, a gyermek ember igaz értékek menti jellemformálására, a közösségi élet ajándékosztó erejének építésére.</p>

			<img class="kep-1 ordering" src="images/stags_logo2.svg">

			<p>A szervezet programja, eszmeisége –törvényeink, fogadalmaink, próbáink, stb.- a magyar hagyománykincsre, történelmi, természeti örökségeinkre épül. Szövetségünk jelvénye is a magyar hagyománykincset idézi: két –korondi motívumból vett- csodaszarvas találkozik a Hármas halom tetején. Egyik a mostani határainkon belüli, a másik a mai határainkon túli magyar gyermekeket jelképezi.</p>

			<p>Találkozásuk a Hármas halom tetején pedig azt jelenti: testvérek vagyunk. Szövetségünk zászlaján a farkasfogak az 1848-49-es szabadságharc emlékét őrzik. A zászló egyik oldalán a szövetség hímzett jelvénye látható, másik oldalán pedig a szabadságharc harmadosztályú érdemjele, az ezüst babérkoszorú, amit a Maros-menti harcokban Szomjas Károly, 13 éves fiú az ügyességéért, szemfülességéért, hősies helytállásáért kapott, amikor észrevette a magyar seregre törő ellenséget, riadóztatta egységét, s az ütközetben biztatásával, lelkesedésével erősítette a felnőtt honvédek kitartását.</p>

			<p>A szövetség és a csapatok programjával ,próbákkal, rendezvényekkel, táborokkal, pályázatokkal, felfedezőutakkal, stb. a gyermekek és a szülőföldjük, hazájuk között olyan aranyszálak szövődnek, amely kötődéssel a gyermek nem csak megtanulja, hogy mennyi értéket rejt szülőföldje, hazája, hanem meg is érzi milyen csodálatosan szép érzést ad szeretni az otthonát, ahol él és a hazáját, ami körülveszi őt. Így érti és érzi át a szövetség Rákóczi kort idéző köszöntését is: „Pro Patria!” és a felelet rá: „Hűséggel!”. A szövetség felkarolta hazánk történelmének, például a	Rákóczi- és az 1848-49-es szabadságharc gyermekhőseit. (Lásd: Rakó József: A szabadságharc gyermekhősei, MH OKAK, 1992.) Megemlékezésekkel, játékokkal, felfedezőutakkal, pályázatokkal ápoljuk emléküket. Történeteiken, tetteiken keresztül a nemzet története így az egykori gyerekek történeteivel kerülhet igazán közel a mai gyerekek szívéhez, lelkéhez.</p>

			<p class="clear">Szövetségünkben a gyermekek elsősorban csapatokban dolgoznak. (Bár van lehetőség az egyéni kötődésre is.) A csapatok jobbára iskolák mellett szerveződnek, de bárhol alakítható közösség –hagyományőrző csapat, kör, stb.-, ahol felnőtt segítő vállalja akár csak három kisgyermek vezetését, csapattá formálását. A felnövekvő nemzedék életajándéka az öntevékenységre alapozó közösség. Ahol együtt lehet szőni merész, romantikus terveket, ahol megtanuljuk mások értékeinek megbecsülését, ahol a barátok segítségével felbukkannak a gyermek ember saját értékei; ahol mindenki megérzi: „fontos vagyok!” Célunk a gyermek, a fiatal értékrendjének, jellemének formálása is, a közjóért végzett munka megszerettetése, a küzdés, az önépítés, az egymás iránti szeretet, odafigyelés bontakoztatása. A jót, a nemest, a derűt keresők szervezete vagyunk. Néha sorsfordító lehet, ha a gyermeknek valaki a szemébe mondja: nagy segítséget jelentett a csapatnak, barátjának, hogy valami nagyszerűt vitt végbe és hogy ő értékes, jó ember… </p>

			<img class="kep-1 ordering" src="images/rolunk/bemutatkozas/gyerekhosok.png">

			<p>Szövetségünk tagja bárki lehet, aki céljainkat, törvényeinket, eszmeiségünket elfogadja, magáénak vallja. A tagság elnyeréséért azonban „felvételi próbát” kell teljesíteni. Ez nem más, mint egy játékos kalandozás a szülőföldön. A felvételi próbázók bejárják szülőföldjüket, otthonuk környékét, megismerkednek annak nevezetességeivel, szépségeivel. Aztán az egyik összejövetelen elérkezik a pillanat, amikor mindenki saját „örökséget” választ magának. Megnevezi azt az emlékhelyet, kis patakot, fát, házat, stb. ami számára a legkedvesebb szülőhelyén. A legfontosabb pillanatok egyike ez, hiszen ekkor egy örökre szóló szövetséget köt szülőföldjével, örökre a szívébe zárja annak egy darabját. Később már maga is keresi, gyűjti „örökségeit”, s így kis idő múlva egész Magyarországhoz, s a határainkon túli magyar vidékekhez is láthatatlan aranyszálak sokasága fűzi majd.</p>

			<p>Aki felvételi próbát tett és „felfedező” lesz, viselheti már egyenruhánkat. Egyenruhánk a galambszürke (szürkészöld) ing, a - korábban háromszögletű, újabban három szálból font- világoskék (égszínkék) nyakkendő és a Bocskai sapka. Fonott nyakkendőnk két szárát a szövetség jelvényével, vagy a csapatjelvénnyel fogjuk össze. Illetve aki megkapta, az „Ezüstszarvas” vagy az „Aranyszarvas” kitüntető jelvénnyel is összefoghatja. (A nyakkendő fonatának három szála közül az egyik a szövetség vezetői, a kisközösségek vezetői számára adományozott elismeréséként lehet fehér színű, ami a szervezet részéről elismerést, kitüntetést, a viselője részéről pedig a szövetség felé tanúsított hűségét jelenti. A fehér szállal font nyakkendő tehát csak vezetői rang és a szövetség tagjaként hűségesen eltöltött idő után, kitüntetésként viselhető.) Az egyen ing bal és jobb oldali zsebén, valamint a kar- és váll lapokon a próbák jelvényei, emlékjelvények, vezetői fokozatok egyéb jelzései, stb. megfelelő szabály szerint kapnak helyet. (Az egyenruha fontos, mert kifejezi összetartozásunkat. Azonban nem kívánjuk meg senkitől, hogy ez olyan kiadást jelentsen, ami gátja annak, hogy a szövetséghez tartozzon. A nyakkendő önmagában elégségesen kifejezi összetartozásunkat, annak viselete mindenképpen fontos. De a nyakkendőnknek is –éppen az összetartozás kifejezésére- olyan hármas fonatot választottunk, amit otthon, az avatás előtt esetleg egy kis segítséggel, valójában bárki elkészíthet.)</p>

			<img class="kep-2" src="images/rolunk/bemutatkozas/ing.png">

			<p class="italic">Felfedezők egyen ingben</p>

			<p class="clear">Legfontosabb országos rendezvényünk a sárospataki központunkban minden évben megrendezett vezetőképző –tisztképző-táborunk. Sárospatakon „életre keltettük” a Magyar Köztársaság címerének Hármas halmát. A történelmünk gyermekhőseinek emléket állító sárospataki múzeumunk kertjében kopjafákkal övezve áll a Hármas halom. Mi gazdagítjuk, gyarapítjuk. A találkozókra ugyanis mindenki hoz magával szülőföldjéről egy maroknyi földet, amit a tábor nyitó ünnepségén valamelyik dombocskára szór és közben elmondja, hogy az a kis föld honnan származik, s ahonnan származik, az a hely miért fontos számára. Így van már a Hármas halom minden halmán föld sok magyarországi magyar gyerek szülőföldjének kedves zeg-zugaiból, s a mai határokon túli Kárpátaljáról, Erdélyből, Felvidékről, Délvidékről is, sőt szarajevói magyar gyerekektől és Belgiumban élő magyar emberektől is. Vezetőképző táboraink hagyományos zarándok útjai vezetnek a Zemplén csodás vidékein álló romantikus romvárakhoz, Sátoraljaújhelyre, Széphalomba a Magyar Nyelv Múzeumába és a határon túli Borsiba. Borsi „a magyar Betlehem” tartja a mondás, hiszen ennek a községnek a ma még romos, de sok magyar ember jóakaratából egyre szépülő kastélyában született II. Rákóczi Ferenc. A táborokban hazánk sok tájáról és a határokon túli magyar vidékekről érkezők között örökre szóló barátságok kötődnek. A táborok adta élményeket, az esti pásztortüzek mellett dalolt népdalokat és egymásnak elmondott „szívhangokat” minden pataki táborozó örök emlékként őrzi. A táborok záró ünnepségét is a Hármas halom előtt tartjuk. Akkor a gyermekek, fiatalok egyenként lépnek a többiek elé és a tábori vezetőjük elmondja azokat a jó tulajdonságokat, amiket a pár nap alatt meglátott a gyermek vagy fiatal „tisztben”. A leendő vezetők pedig a Hármas halom előtt, a szövetség zászlaját megérintve mondják el vezetői fogadalmukat s kapják meg a teljesített próba igazolását, jelvényét.</p>

