			<p>A felvételi próbát követő avatás után mindenki kedvére választhat például a régmúlt erősségeit kutató „várfelderítők”, a természet szépségeit kereső „vándordiákok”, a néphagyományok nyomába eredő „igricek”, a „diáktűzoltók”, „veszélyelhárítók”, a „Kossuth regimentje”, a „Rákóczi hagyományőrzője” és sok más izgalmas próba, felfedezőút, pályázat és játék közül. A legtöbb próbának van tiszti fokozata is, ami már kicsit a mások segítésére, az adott terület másokkal való megismertetésére is irányítja a próbázót.</p>

			<img class="kep-2" src="images/rolunk/jelvenyek/alap.png">

			<p class="italic">Alappróba jelvényeink</p>

			<p>A szövetség vezetői fokozatait szintén tiszti próbák, fokozatok jelentik. A vezetőket azért hívjuk „tiszteknek”, mert a sorsdöntő történelmi pillanatokban a tisztek voltak azok, akiket a hozzájuk tartozók bizalommal követtek, akik példamutatásukkal, emberségükkel, hazaszeretetükkel méltó példaképei, felelős vezetői voltak a rájuk bízott embereknek, sokszor gyerekeknek is. Így szerezhető a szövetség vezetői próbájaként korhatártól is függően őrsvezetői, őrstiszti, zászlóaljtiszti, törzstiszti és vezetőképző tiszti rang, fokozat is.</p>

			<img class="kep-2" src="images/rolunk/jelvenyek/tiszt.png">

			<p class="italic">Csapatvezető tiszti jelvényeink</p>

			<p>A próbák mellett kitüntetések, elismerések várják szövetségünk tagjait. Valljuk, hogy az elismerés felemel. Mindenekelőtt azt, aki vállalja a munkát. De büszke lehet rá a környezete –kis közössége, barátai, szülei, iskolája- is. A kitüntetések értékerősítő elismerések. Számos kitüntetésünk alapítói, gondozói történelmi családok mai leszármazottai, vagy más, munkánkat becsülő országos szervezetek. </p>

			<img class="kep-2" src="images/rolunk/jelvenyek/kituntetesek.png">

