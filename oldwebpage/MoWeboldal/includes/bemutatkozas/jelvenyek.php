			<p>Városok, önkormányzatok is szívesen részt vesznek munkánkban, néhány közülük kitüntetést is alapított a szövetség tagjai részére. Sárospatak Város Önkormányzata például minden évben üdvözli és figyelemmel kíséri a nyári táborok munkáját, s a város vezetése minden évben átadja a „Sárospatak hagyományőrzője” elismerő jelvényt, emléklapot a kitüntetésre javasolt felfedezőknek.</p>

