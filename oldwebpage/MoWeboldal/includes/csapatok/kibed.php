				<h3>Tevékenységeinkből</h3>

				<p><u>Felfedezőutak:</u> történelmi és kulturális múltunk értékeinek megismerése céljából. Így a következő helységekből gyűjtöttünk elkobozhatatlan kincseket (örökségeket): Kibéd, Hármasfalu, Makfalva, Sóvárad, Szováta, Parajd, Korond, Farkaslaka, Székelyszentmihály, Székelyudvarhely, Agyagfalva, Székelykeresztúr, Héjjasfalva, Fehéregyháza, Segesvár, Csíkszereda, Madéfalva, Gyimesi-szoros, Marosvásárhely, Radnót, Kolozsvár, Tordai-hasadék, Nagyenyed, Sepsiszentgyörgy, Kisbacon, Gyergyószárhegy, Gernyeszeg, Marosvécs, Bonchida, Pusztakamarás, Szeben, Vízakna, Gyulafehérvár, Vajdahunyad, Déva, Geges. Magyarországon Budapesten, Gyömrőn valamint a sárospataki táborozások alkalmával tettünk még sok felfedező utat. Két csapattagunk Felsőszeli és környéke megismerésében vett részt a többi erdélyi csapat szervezésében a felvidéki csapat meghívására.</p>

				<p><u>Sárospataki táborozások</u> alkalmával minden évben több csapattag különböző próbákat teljesített. Bizonyítják ezt az emléklapok és kitűzők. A Sárospatak hagyományőrzője kitüntetést eddig több mint 10 csapattag érdemelte ki. Törzstiszti próbát tettek az évek során: Madaras Emőke, Kovács Hont Imre, Domo Anna-Bella tanítónő és Szilveszter Eszter aki az utóbbi években a táborban ifjú vezetőként is tevékenykedik.</p>

				<img class="kep-2" src="images/kibed/foldadomany.png">

				<p class="italic">Földadományozás a Szövetség sárospataki múzeumának udvarán</p>

				<p>Földadományokat szintén minden évben szórtak le a mindenkori táborozóink Sárospatakon. Ezeket általában felfedező útjainkon gyűjtöttük, így gazdagítva a sárospataki hármas halmot. </p>

				<p><u>Történelmi, anyanyelvi és igric játékokat</u> minden évben szervezünk egy-két alkalommal és az utóbbi években együtt van ilyenkor a négy erdélyi csapat. Ezeket a játékokat rendszerint Sárospatakon is megismételjük, sőt Gegesben is.</p>

				<p><u>Műsoros előadásokat</u> is minden évben tartunk március 15, október 6.-án, illetve más történelmi évfordulók vagy éppen hagyományos ünnepségek alkalmával. Megemlékező műsorainkból Sárospatakon, Széphalomban az alábbiakat ismételtük meg:</p>

				<ul class="ulv2">
					
					<li>Nemes ifjak társasága megalakulásának 300. évfordulójára (2007)</li>
					<li>Kazinczy Ferenc nyelvújító (2008)</li>
					<li>Trianon (2010)</li>
					<li>Mikes és a Rákóczi szabadságharc (2011)</li>
					<li class="pad">Szabó Jóska emléke </li>

				</ul>

				<img class="kep-2" src="images/harmasfalu/nepitanc.png"> <br>

				<p>2017-ben szövetségünk nevében Kolozsváron ünnepeltük meg a Nemes Ifjak Társasága megalakulásának 300. évfordulóját: 4 „nemes ifjú” és 6 új csapattag tett akkor fogadalmat.</p>

				<p><u>Csapatfoglalkozásaink</u> programját próbáljuk minél színesebbre varázsolni néphagyományok megismerésével, történelmi események áttekintésével, rovásírás gyakorlásával és sok dalolással. A rovásírás megismerése és elsajátítása céljából sok játékos foglalkozást szerveztünk évente, amiknek köszönhetően minden csapattag ismeri a rovásírást. Új dalok tanulása rendszeres tevékenységünk. Eddig 6 kuruc dalt, 10 „negyvennyolcas” dalt és számos népdalt ismerünk.</p>

				<p>A 2000-es évek elején Kibédet bemutató <u>írásainkból</u> Kossuth Rádióban hangzott el válogatás Indri Gyula „Ifjúsáv” című műsorában. Havonta jelennek meg a csapatvezető írásai a Kibédiek lapjában.</p>

				<p><i>Erdély felfedezőiként</i> Makfalva, Hármasfalu és Sóvárad csapataival 2012 óta általában közös tevékenységekben is részt veszünk.</p>

				<p>Nagyon hálásak vagyunk a Magyarország Felfedezői Szövetség vezetőségének a sok szakmai támogatásért és a sárospataki táborozásokért. Valamint kegyelettel emlékezünk a belgiumi <b>Horváth Vilmosra</b> <i>(Vili bácsira)</i>, aki éveken át anyagi támogatásával segítette csapatunkat felfedező utak megszervezésében.</p>

