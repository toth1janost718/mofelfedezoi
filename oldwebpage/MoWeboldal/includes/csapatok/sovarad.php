	<h2 class="CIM2">A Só útjának felfedezői</h2>

		<p class="h5v2"></p>

		<div class="container">

	<div class="row">

		<div class="col-12 col-s-12">

				<p class="italic">

					„Amit jól szeretsz, maradandó lészen. Amit jól szeretsz, való örökséged.” (Kovács András Ferenc)

				</p>

				<img id="badge" src="images/sovarad/kituzo.png">

				<p>A sóváradi felfedezők csapata a 2012 – 2013-as tanévben alakult az akkori alsó tagozatos gyerekek kis közösségéből. Az érdeklődő tanulók kíváncsian vettek részt a bemutató tevékenységeken, és lelkesedéssel készítették el avatási próbalapjukat. Első pillanattól kezdve erős háttért biztosított a születendő csapatnak az akkor már létező hármasfalusi és kibédi hagyományőrző csapatok támogatása. Végül 2013 júniusában kilenc tag avatásával indult útjára a szövetség sóváradi alegysége.</p>

				<p>Az erdélyi csapatok élete a továbbiakban is összefonódott. Falufelfedező, hagyományőrző játékokon, kirándulásokon vettünk részt, kiemelkedő történelmi eseményekre emlékeztünk meg együtt, vetélkedőket is szerveztünk, melyen a csapatok összemérhették felkészültségüket, rátermettségüket, tudásukat.</p>

				<p>A Só útjának felfedezői évről évre gyarapodtak közös élményekben és létszámban.</p>

				<p>Tevékenységeinken azokat a kincseket keressük, amelyek személyes életünket, lelkünket gazdagítják, szorosabban összefűznek egymással és a szülőföldünkkel, példaképeket keresünk a már letűnt idők hétköznapi és nemzeti hősei személyében. Bár a múltból merítünk ihletet, a jövőre készülünk.</p>

				<p>Kilencedik, szabadon választott törvényünket így fogalmaztuk meg: <i>„Elődeink életmódját igyekszem megismerni, felhasználni, megélni, jó példájukat követni a mindennapi életemben.”</i> Ehhez híven fedeztük fel őseink hétköznapi szokásait, munkálatait, ünnepeit, és csodálkoztunk rá a józan paraszti ész diktálta életmódra, melytől mára már annyira eltávolodtunk.</p>

				<p>Lényeges szempont a csapat életében a magyar kultúra ma is élő, lüktető vérkeringésébe való bekapcsolódás. Megismerkedtünk az idők folyamán változó székely és magyar himnuszokkal, rácsodálkoztunk a versek, dalok, szobrok, festmények, jelképek közötti összefüggésekre. Elolvastunk regényeket, majd megnéztünk azok színházi feldolgozásait is.</p>

				<img class="kep-2" src="images/sovarad/csapat.png">

				<p class="italic">A grundon a Pál utcai fiúkkal (Marosvásárhelyi Nemzeti Színház)</p>

				<p>Fontos számunkra a szülőfalu megismerése földrajzilag és történelmileg egyaránt. A szövetség hetedik törvénye szerint mi is építeni, gyarapítani szeretnénk a faluközösséget, ahova tartozunk. 2015-ben egyik csapattagunk nagytatája, Józsa András helytörténész vezetésével kutattuk fel a sóváradi temetőben az évszázados sírköveket, és találtuk meg öt, ’48-as honvéd sírját.</p>

				<img class="kep-2" src="images/sovarad/kutatas.png">

				<p class="italic">Honvéd sírok felkutatása a sóváradi temetőben</p>

				<p>Az újabb érdeklődő csapattagok számára szervezett ismerkedő tevékenységekben, játékokban nagy segítséget nyújtanak a már régebbi tagok. Legnagyobb sikernek a kézműves tevékenységek, valamint a szabadtéri programok örvendenek.</p>

				<p>A Só útjának felfedezői szabadidejükben is megélik fogadalmukat: <i>„Kitartóan erősítem a szeretetet, amely családomhoz, barátaimhoz fűz.”</i></p>

