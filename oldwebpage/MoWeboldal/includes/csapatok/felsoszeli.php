			<p>A csapat Magyarország Felfedezői Szövetséggel való együttműködése 2003-ban kezdődött, amikor 6 diák jutalomként egy táborozást nyert a szövetség Vezetőképző táborába, melyre Sárospatakon került sor. Azóta a felvidéki diákok minden évben ellátogatnak a táborba, ahová nem csak az élmények, hanem az évek alatt kialakított barátságok is visszacsalogatják a csapat tagjait.</p>

			<p>Az erdélyi csapatokkal kialakított remek kapcsolat eredményeként 2016 nyarán 6 diák és 2 tanár juthatott el a Kis-Küküllő melletti Geges településben megszervezett felfedezőtáborba, ahol ismerős arcokkal tölthettek el 6 kellemes napot. A tábor sikerességén felbuzdulva és a meghívás viszonzásaként idén nyáron július 5-10. között Felvidéken, Gímesen került megrendezésre az I. Felvidéki Felfedezőtábor, ahová az erdélyi barátok is meghívást kaptak.</p>

