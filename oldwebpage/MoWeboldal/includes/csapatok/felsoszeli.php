			<p class="clear">A folyamatos építő tevékenység mellett elmondhatják magukról, hogy a tagok száma minden évben nő. Új tagokat minden tanévben ünnepség keretein belül avatnak. A csatlakozni kívánó diákoknak fogadalmat kell tenniük, hogy igyekeznek megismerni és ápolni a hagyományokat, a magyar történelmet valamint egyéni fogadalmat is tesznek, mindenki a saját lehetősége szerint.</p>

			<img class="kep-2" src="images/felsoszeli/avatas.png">

