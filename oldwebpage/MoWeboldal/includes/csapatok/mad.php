	<h2 class="CIM2">Koroknay Dániel Hagyományörző Csapat</h2>

		<p class="h5v2">Csapatvezető: Gáspárné Késmárki Emília</p>

		<div class="container">

	<div class="row">

		<div class="col-12 col-s-12">

			<p>Csapatunk 1998-ban alakult meg 24 lelkes gyermekkel. Nevünk azon 14 éves fiúhoz kötődik, aki Mádon állt be a seregbe. Először az 5. majd a 11. gyalogütegben szolgált és később elnyerte a főágyúsi rangot. Vagyis ő adott parancsot az ágyú tüzelési állapotba helyezésére és ő maga irányozta be a csövet a célpontra.</p>

			<p>Ezt a csapatunk tagjai is értik és igazi példaképként kezelik a hős fiút. Valamint Mád is méltó emléket állított Koroknay Daninak, ugyanis az iskolában állandó kiállítás, emléktábla őrzi nevét. 2000-ben a Millennium évében az iskola felvette a fiú nevét.</p>

			<img class="kep-2" src="images/mad/csapat.png">

			<p>Csapatunk éves programjába beletartozik a március 15.-ei fáklyás felvonulás. Mivel ez a megalakulásunk dátuma, elmaradhatatlan Koroknay Dániel emléktáblájának megkoszorúzása. Ez a tábla az iskola falán található és minden évben nagy tömegek tekintik meg ezt a rövid szertartást.</p>

			<p>Az október 6.-ai ünnepség is kiemelkedő ránk nézve. Községünk temetőjében fekszik Pándy Soma Sámuel és Cornides Lajos is, akiknek minden évben megszervezzük a megemlékezést, általában egy kis műsorral. Számunkra nagy elismerés, hogy Mád vezetői, intézményvezetők és a támogatóink is részt vesznek rajta</p>

			<p>Iskolánk minden évben megrendezi a Koroknay napokat. Ilyenkor történelmi hadijátékokat tartunk. Ez jó lehetőség a toborzásra is. Valamint új programunk keretében a kisebb diákok egy körutazást tehetnek Zemplén megyében. Ellátogatnak Sárospatakra, Széphalomra és Boldogkőváraljára is.</p>

			<p>Újdonsült tagjainkat igyekszünk gyorsan bevonni a munkába. A felavatásuk után, amely a Mádi Református Templom udvarában zajlik, rögtön a falu hagyományaival és nevezetességeivel ismertetjük meg őket.</p>

			<img class="kep-2" src="images/mad/templom.png">

			<p>Később kitárjuk ezt a kört Magyarországra is. Így a nyári Sárospataki táborra már felkészülten érkeznek.
			De nem csak a munka van előtérben. A délutáni csapattalálkozókon gyakran szervezünk kézműves foglalkozást és vetélkedőket is. Nagy népszerűségnek örvendenek a falu kis utcáiban megszervezett hadijátékok. Ezek által számos téma megismertethető a gyerekekkel játékos formában. Közben mozognak és bejárjak lakóhelyüket is.</p>

			<p>Legjelentősebb elismerésünk az Örökség Serleg átvétele 2014. január 19-én, a XVIII. Magyar Kultúra Napja Gála keretén belül megrendezésre kerülő eseményen.</p>

			<p>Tizenhárom országból érkeztek vendégek Budapestre a XVIII. Magyar Kultúra Napja Gálára, ahol 21 személy részesült a Kultúra Lovagja elismerésben. Az elismerésre civil szervezetek és önkormányzatok állítottak jelölteket a magyar és az egyetemes kulturális örökség ápolása, közkinccsé tétele, alkotó fejlesztése, a kultúrák nemzetközi együttműködésének elősegítése, valamint kulturális tevékenység támogatása érdekében huzamos időn át kifejtet önzetlen tevékenységért.</p>

			<img class="kep-2" src="images/mad/kituntetes.png">

			<p class="italic">A kitüntetett csapattagok oklevelet, ezüst kitűzőt és érmet kaptak, nevüket bejegyzik a Magyar Kultúra Lovagjai Aranykönyvébe.
			Továbbra is igyekszünk ápolni Mád és Magyarország hagyományait, gyermekeink is aktívan és szorgalmasan teljesítenek minden évben, hiszen a jutalmuk egy hét Sárospatakon, a szövetség táborában.</p>

