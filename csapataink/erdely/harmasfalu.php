        <h2 class="cim2">Szent István utódai hagyományőrző csapat</h2>
        <p class="cim1">Csapatvezető: Bereczki Erzsébet</p>
        <h4 class="cim2">2010-2011</h4>
        <p>A „Szent István utódai” hagyományőrző csapat 2010. szeptember 25-én kezdte le tevékenységét 25 gyerekkel. Első csapattevékenységünk alkalmával bebarangoltuk szülőfalunk, Hármasfalu mindhárom faluját: Atosfalvát, Szentistvánt és Csókfalvát. Megcsodáltuk 6 szép templomát, szobrait. A nagy király egész családja megtalálható e három kis faluban: Szentistvánon Szent István király szobra, Csókfalván Szent Imre hercegé, Atosfalván pedig Gizella királyné. Sok gyerek már ekkor kiválasztotta szülőföld örökségét.</p>

        <p>Tevékenységeink során, miközben a szövetség törvényeivel, jelképeivel és Rákóczi korával ismerkedtünk, felelevenítettünk, eljátszottunk néhány helyi néphagyományt, népszokást: a szüreti bált és a fonót. A lányok meg tanultak fonni. Előkerültek a padlásokról a nagymamák orsói, guzsalyai és megtapasztalhattuk, milyen fáradságos munka volt a fonás.
          Avatási ünnepségünkre 2011. március 15-én került sor iskolánkban, a „Szent István” Általános Iskolában. Ekkor 21 gyerek tette le a fogadalmat. Jelen volt a kibédi „Mátyus István” hagyományőrző csapat is, ők is avattak 3 új tagot.</p>

          <p>2011 tavaszán történelmi játékon vettünk részt Kibéden. A játék után Mátyus Ilonka néni kalauzolt végig minket a falun, megmutatva Kibéd szépségeit, értékeit: a református templomot, benne a Bodor Péter-orgonát, Seprődi János szülőházát, Madaras Gábor, Seprődi János és Mátyus István szobrát. A nap egyik fénypontja volt a Ráduly Jánosnál tett látogatás, mely során a híres népmesegyűjtő megmutatta dolgozószobáját, mesét olvasott, könyvet ajándékozott és dedikált nekünk.
            2011. július 25.-augusztus 1. között a csapat 3 tagja részt vett a vezetőképző táboron, Sárospatakon. Rengeteg szép élménnyel tértünk haza. Köszönjük a szövetség vezetőinek!
          </p>

          <h4 class="cim2">2011-2012</h4>

          <p>Október 6-án csapatunk az aradi vértanúkra emlékezett műsorával a Szent István Általános Iskola közösségében. Novemberi, decemberi csapattevékenységeinken gyöngyfűzéssel, valamint a rovásírás rejtelmeivel foglalkoztunk. 2012. januárjában tartottunk is egy rovásírás- vetélkedőt.</p><span class="image left"><img src="../../images/csapat/harmasfalu/mar15.png" alt="Rovásírás vetélkedő" title="Rovásírás vetélkedő" /></span>
          <p>Februárban 3 gyerek kezdte el készítgetni a próbafeladatokat, hogy március 15-én letegye a szövetségi fogadalmat. Március 15-én ünnepi műsorunkkal és kiállításunkkal a szabadságharc gyermekhőseire emlékeztünk. E napon tette le a szövetségi fogadalmat 3 gyerek.</p>

          <p>Április 4-én takarítási akciót szervezett a csapat. A Csókfalván található Szent Imre herceg szobra körüli parkot takarítottuk ki, tettük széppé. Munkánk végeztével Szent Imre herceg lábai elé telepedtünk, népdalokat és `48-as dalokat énekeltünk, majd a tápióbicskei csatára és annak gyermekhőseire emlékeztünk.</p>

          <p>A Szent Imre parkot csapatunk szülőföld örökségének választotta.</p>

          <p>Május 5-én néptánc fellépésünk volt a Székely Majálison Makfalván. A rendezvény ünnepélyes felvonulással kezdődött: fúvószenekarral, huszárokkal és népviseletbe öltözött gyerekekkel, fiatalokkal. Fellépésünk után kézműves foglakozásokon vettünk részt.</p>

          <p>A májusi hónap további részében a pályázatkészítéssel voltunk elfoglalva. Igazán színes, változatos munkák készültek: csatajelenetet és tábori, katonai életet ábrázoló rajzok, emléktábla- terv, díszoklevél Szabó Jóska részére, vers, faragott kopjafa, népdal-átköltés.</p>

          <p>Május 23-án ismét virágokkal szépítettük Szülőföld örökségünket, a Szent Imre-parkot.</p>

          <p>Június 9-én történelmi játékot szerveztünk Szabó Jóska emlékére. Vendégeink voltak a kibédi "Mátyus István hagyományőrző csapat" tagjai. A játék után Kossuth-nótát és csíki regrutadalt tanultunk. Kellemes, tartalmas napot töltöttünk együtt, melynek során mindannyian gazdagodtunk lelkiekben, szellemiekben.</p>

          <p>Július 30-augusztus 5.között Sárospatakon vezetőképző táboron vett részt a csapat 6 tagja. A táborban megismételtük a Szabó Jóskáról szóló játékunkat a gáton, valamint egyik napzárta alkalmával bemutattuk csapatunkat. Csodálatos élményekben volt részünk, már várjuk a következő nyarat.</p>

          <h4 class="cim2">2012-2013</h4>

          <p>Október 7-én az aradi vértanúkra emlékeztünk a szentistváni református templomban, az istentisztelet keretében. A műsor után az időseket köszöntöttük fel és ajándékoztuk meg egy kis magunk készítette könyvjelzővel. Ugyancsak októberben, 27-énigricjátékot játszottunk Csókfalván, melyen részt vettek a sóváradi gyerekek . Az igricjáték után csapatunk szülőföld örökségéhez, a Szent Imre parkhoz sétáltunk, ahol énekléssel és népi játékokkal tettük kellemesebbé és emlékezetesebbé ezt a szép napot. Nagyon örültünk a sóváradi gyerekeknek és reméljük, megtalálják helyüket a Szövetségben.<span class="image right"><img src="../../images/csapat/harmasfalu/nepitanc.png" alt="Néptánc" title="Néptánc" /></p>

          <p>November 2-án, február 1-jén és 8-án, április 2-án népi játszóházat szerveztünk az I-IV. osztályosok számára. A játszóházak témája: kukoricafejtő kaláka, fonó, farsangtemetés és húsvéti tojásírás, fenyőágdíszítés.</p>

          <p>Március 15-éna szabadságharcra emlékeztünk. Az ünneplés felvonulással kezdődött, ezután koszorút helyeztünk el Szentistvánon, majd előadtunk ünnepi műsorunkat a csókfalvi unitárius templomban. Május 4-én néptánc fellépésünk volt a Székely Majálison Makfalván. A rendezvény ünnepélyes felvonulással kezdődött: fúvószenekarral, huszárokkal és népviseletbe öltözött gyerekekkel, fiatalokkal.</p>

          <p>Június 1-jén közös felfedezőútra indultunk Kibéd és Sóvárad csapataival a következő útvonalon: Nyergestető, Kézdivásárhely, Zabola, Zágon, Sepsiszentgyörgy, Kisbacon. A Nyergestetőn szövetségi fogadalmat tett 2 hármasfalusi és 10 sóváradi gyerek.</p>

          <p>Július 28-augusztus 6. között vezetőképző táboron vett részt a csapat 4 tagja Sárospatakon. A táborban közösen a többi erdélyi csapattal beszámoltunk felfedező utunkról.</p>

          <h4 class="cim2">2013-2014</h4>

          <p>Október 6-án csapatunk ünnepi műsorral az aradi vértanúkra emlékezett a Szent István Általános Iskola közösségében. Október 9-én néptác fellépésünk volt Gegesben az almafesztiválon.</p>

          <p>Október 15-én történelmi játékon vettünk részt Sóváradon, melyen felelevenítettük az 1848-49-es szabadságharc eseményeit.</p>

          <p>Október 31-én, november 2-án és január 30-án népi játszóházat szerveztünk az I-IV. osztályosok számára. A játszóházak témája: katonabúcsúztatás, kukoricahántó kaláka és kézimunkázó kaláka.</p>

          <p>2014. március 15-én a szabadságharcra emlékeztünk. Az emlékműsort a szentistváni református templomban mutattuk be, majd sor került 7 gyerek avatására az aradi vértanúk emlékére állított kopjafák előtt. Ezután részt vettünk a makfalvi “Wesselényi nyomdokain” hagyományőrző csapat műsorán a makfalvi református templomban, ahol szövetségi fogadalmat tett 10 makfalvi gyerek.
            Május 10-én “Hármasfalu felfedezése” címmel játékot szerveztünk, melyen részt vettek a kibédi és a makfalvi csapatok tagjai.</p>

            <p>Június 21-én “Kibéd felfedezése” című játékon vettünk részt Kibéden a sóváradi és makfalvi csapatokkal együtt.</p>

            <p>Június 26-ánközös felfedezőútra indultunk Kibéd, Makfalva és Sóvárad csapataival a következő útvonalon: Szárhegy, Madéfalva, Gyimesbükk, Csíkszereda, Szent Anna tó, Mohos-láp. Gyimesbükkben szövetségi fogadalmat tett egy makfalvi és egy kibédi gyerek.</p>

            <p>2014. július 28.-augusztus 5. között vezetőképző táboron vett részt a csapat 4 tagja Sárospatakon. A táborban, közösen a többi erdélyi csapattal, beszámoltunk felfedező utunkról.</p>

            <h4 class="cim4">2014-2015</h4>

            <p>Október: A négy erdélyi csapat nagyrészt együtt szervezi tevékenységeit.
              Az idei tanévben első közös tevékenységünk október 6-hoz kapcsolódott. Egy egész tevékenységsorozattal emlékeztünk az aradi 13-ra. A kibédi csapat 5-én, vasárnap a református templomban mutatta be műsorát Mátyus Ilonka vezetésével. A hármasfalusi csapat 6-án, hétfőn az iskola közösségével, a tavaly avatott kopjafák előtt emlékezett rövid műsorral. Az emlékezést 11-én zártuk az Aradra emlékezünk című történelmi vetélkedővel. Ezen a vetélkedőn mindenik erdélyi csapat részt vett.
            </p>

            <p>2015. február 5-7 között meglátogatott bennünket a gyömrői csapat néhány tagja. Ez alkalomból több közös programot szerveztünk: műsorok, játékok, falufelfedezések (Hármasfalu, Makfalva, Kibéd, Sóvárad, Szováta felfedezése), szánkózás, közös alvás.</p>

            <p>Így szólt a dal 2015. március 15-én a szentistváni református templomban a „Szent István utódai” hagyományőrző csapat ajkáról:</p>

            <p  style="color:#008000;text-align:center">„Hej, tulipán, tulipán,<br>
              Ez a hely az én hazám.<br>
              Ide jöttem világra,<br>
              Remélem, nem hiába.” </p>

              <p> Megalakulásunk óta, immár négy éve kötelességünknek érezzük minden nemzeti ünnepen, emléknapon a múlt eseményeinek felidézését, a hősök előtti tiszteletadást. Így történt ezen a március 15-én is: istentisztelet után a csapat dalokkal, versekkel, a szabadságharc eseményeinek felidézésével kezdte az ünneplést. Ezután az aradi vértanúk emlékére állított kopjafáknál énekeltük a Szövetség indulóját, a Kossuth-indulót. Ünneplésünket koszorúzással zártuk. A kopjafáknál elhelyezett koszorúnk felirata:</p>

              <p class="cim1" style="font-style:italic">Pro patria! - Hűséggel!</p>

              <p>Április 14-én makfalvi, hármasfalusi, kibédi és sóváradi gyerekekkel közös felfedezőútra indultunk. Marosvásárhely közelében csatlakozott hozzánk egy paniti gyerekcsapat is: így közel 60 gyerek és 7 pedagógus gyűjtötte ezen a napon a szellemi örökséget a Mezőségen. Állomásaink: Kolozsvár, Válaszút, Bonchida, Pusztakamarás. Kolozsváron szövetségi fogadalmat tett egy hármasfalusi gyerek.</p>

              <p>Május 9-énnéptánc fellépésünk volt a Székely Majálison Makfalván. A rendezvény ünnepélyes felvonulással kezdődött: fúvószenekarral, huszárokkal és népviseletbe öltözött gyerekekkel, fiatalokkal.</p>

              <p>2014. július 28.-augusztus 5. között vezetőképző táboron vett részt a csapat 4 tagja Sárospatakon. Sok élménnyel tértünk haza.</p>

              <p>Augusztus 11-13. között Gegesben szerveztünk hagyományőrző-felfedező tábort a sárospataki tábor mintájára.</p>

              <h4 class="cim2">2015-2016</h4>

              <p>Szokásához híven október 6-án csapatunk ünnepi műsorral az aradi vértanúkra emlékezett a szentistváni református templomban, majd a vértanúk emlékére állított kopjafáknál. Ugyanígy emlékeztünk március 15-én is.</p>

              <p>Ez év júniusában sem maradhatott el közös felfedezőutunk, amikor együtt a négy erdélyi csapat Bem apó nyomába eredt a szászok földjén. A következő helyeken álltunk meg emlékezni: Berethalom, Szeben, Vízakna és Szelindek. Sok szép emlékkel, élménnyel tértünk haza.</p>

              <p>Júliusban ismét Gegesben táboroztunk, ezúttal vendégeink voltak a felvidéki, felsőszeli Bercsényi Miklós hagyományőrző csapat tagjai is.</p>

              <p>A sárospataki vezetőképző táborban ebben az évben csak egy fiatal képviselte a hármasfalusi csapatot, ő már vezetőként segítette a tábor munkáját.</p><span class="image fit"><img src="../../images/csapat/harmasfalu/csapat.png" alt="Csapat" title="Csapat" />
              <h4 class="cim2">2016-2017</h4>

              <p>Október 1-jén részt vettünk a “Bekecsi csillagtúrán”, amely során az I. világháború egyik ott lezajlott csatájára emlékeztünk.</p>

              <p>Október 6-án, mint mindig, most is az aradi vértanúkra emlékeztünk a református templomban, az istentisztelet után, majd a kopjafáknál koszorúztunk.</p>

              <p>Október 29-én részt vettünk az almafesztiválon Gegesben, ahol egy rövid toborzó játékot is rendeztünk.</p>

              <p>Március 15-én a szabadságharc hőseire emlékeztünk műsorral és játékkal.</p>

              <p>Április 23-án, közös felfedezőutunk alkalmával “Királyok, fejedelmek nyomába eredtünk a következő útvonalon: Nagyenyed, Gyulafehérvár, Vajdahunyad és Déva. Vajdahunyad várában avatási ünnepséget tartottunk: 6 hármasfalusi és egy makfalvi gyerek tette le a szövetségi fogadalmat. Tartalmas és tanulságos út volt.</p>

              <p>Június 4-én műsorral emlékeztünk a gyászos trianoni döntésre és annak következményeire.</p>

              <p>Júliusban Felvidékre látogattunk el, ahol Gímesen táboroztunk a szeli csapattal együtt. Gyönyörű, élmény dús napok voltak. Bebarangoltuk a környék szép helyeit: Nyitrát, Kistapolcsányt, a gimesi várat, Gimeskosztolányt.</p>

              <p>Július 28-augusztus 7. között Sárospatakon táboroztunk, újabb szép élményeket gyűjtve. Nagy élmény volt az utunk Vizsolyba, Boldogkővárra, a Honfoglaláskori Látogató Központba, a Bodrogra.</p>

              <p>A táborok sorát a gegesi táborral zártuk augusztus közepén.</p>

              <h4 class="cim2">2017-2018</h4>

              <p>Október 6-án nem a szokásos módon emlékeztünk az aradi vértanúkra. Ez alkalommal egy olyan kis könyvecskét készítettünk, melyben elhelyeztük valamennyi vértanú képét, nevét, hogy bármikor könnyen fellapozhassuk. Természetesen a koszorúzás a kopjafáknál nem maradt el.
                Az 56-os forradalomról is megemlékeztünk egyik csapatfoglalkozásunk során. Négy fős csapatokban dolgozva a gyerekek plakátokat készítettek ezzel a címmel: <u>Mit jelent számomra 1956.</u> </p>


